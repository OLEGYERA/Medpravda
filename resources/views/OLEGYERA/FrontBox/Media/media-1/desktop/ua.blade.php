            <h2>
                Як впоратися з попрілостями та пелюшковим дерматитом?
            </h2>
            <p>При обох цих станах першочергове завдання - полегшити дискомфорт дитини, а також знизити ризик розвитку інфекції. Для цього потрібно вибрати оптимальний за дією засіб, який допоможе підсушити ушкоджену ділянку, прибрати ознаки запалення й зменшити страждання малюка від болю. </p>
            <p>
                Якщо це попрілості, то потрібно особливо ретельно стежити за режимом зміни підгузків. Вологі серветки слід залишити на випадок прогулянки або походу до гостей. Шкіру на сідничках та в паху помийте під проточною водою, добре промокніть, а потім нанесіть підсушувальний крем. Цинку оксид, що входить до складу Судокрема, пом’якшує, знезаражує та підсушує ділянки з <a href="#link-4"><span class="under-title">проявами запалення</span><sup>4</sup></a>. Лікарі рекомендують крем з вмістом оксиду цинка для лікування попрілостей, пелюшкового дерматиту навіть <a href="#link-2"><span class="under-title">новонародженим</span><sup>2</sup></a>. Судокрем є гарним вибором також тому, що легко наноситься та «не перевантажує» тонку шкіру дитини, це дозволяє наносити крем тонким шаром <a href="#link-4"><span class="under-title">до 6 разів на добу</span><sup>4</sup></a>. Інші компоненти у складі крему теж виконують важливі функції: бензиловий спирт знезаражує шкіру від бактерій і грибків; бензилбензоат зменшує <a href="#link-4"><span class="under-title">почервоніння та біль</span><sup>4</sup></a>; допоміжні речовини: віск та ланолін формують водовідштовхувальну основу, що допомагає створити захисний шар від контакту з подразниками. Крім того, крем не «стягує» шкіру, має ненав'язливий запах. Використання Судокрему сприяє загоєнню уражених ділянок та відновленню шкіри. Внаслідок полегшення симптомів пелюшкового дерматиту та попрілостей поліпшується загальний стан дитини, її самопочуття та <a href="#link-3"><span class="under-title">настрій</span><sup>3</sup></a>. Крім пахової області, Судокрем можна наносити малюкові за вушка, на підколінні ямки та на шийні складки, якщо там виникає почервоніння, пов’язане з показаннями. Не можна наносити Судокрем на слизову та <a href="#link-4"><span class="under-title">відкриті рани</span><sup>4</sup></a>.
            </p>
