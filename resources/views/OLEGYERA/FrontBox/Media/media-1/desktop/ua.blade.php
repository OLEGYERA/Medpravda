            <p>Під час догляду за новонародженою дитиною багато мам зустрічаються з попрілостями на сідничках та в паху.</p>

            <h2>Чому у малюків часто виникають проблеми зі шкірою?</h2>
            <p>Шкіра малюка відрізняється від шкіри дорослої людини. Вона <a href="#link-1"><span class="under-title">тонше</span><sup>1</sup></a>, містить більше води та менше ліпідів, тому більш чутлива та ніжна.</p>

            <h2>До чого це веде?</h2>
            <p>Захисна функція шкіри недосконала, легко виникають пошкодження шкірних покривів, можливі інфекційні ураження шкіри.</p>
            <p>В деяких місцях на шкірі малюка захисний шар зовсім стоншений, наприклад, в області паху. Це впливає на здатність шкіри підтримувати слабокислий рН, який захищає шкіру від інфекції.</p>

            <h2>Коли можуть виникати почервоніння шкіри малюка?</h2>
            <div class="select">Наприклад у випадку попрілостей та пелюшкового дерматиту</div>
            <p>
                Попрілості можуть виникати при порушенні режиму зміни підгузників, якщо міняти їх рідше, ніж рекомендовано виробниками підгузків. Всередині підгузка температура та вологість вище, що може призвести до запалення: шкіра червоніє та стає гарячою на дотик. Попрілості створюють дискомфорт малюку, він може погано спати, їсти та часто плакати. Необхідно вжити заходи, оскільки в місці запалення шкіра буде легко пошкоджуватися інфекційними агентами та бактеріями, грибками. Також пелюшковий дерматит може виникнути у відповідь на постійне подразнення: можливо, дитині не підходить пральний порошок або мило, яким перуть її одяг. Симптоми схожі з тими, що виникають при попрілостях: почервоніння, занепокоєння малюка, але область почервоніння виглядає вологою. У разі приєднання грибкової або бактеріальної інфекції запалення швидко поширюється на стегна та живіт. В таких випадках слід обов’язково звернутися до лікаря.            </p>
