            <h2>
                Как справиться с опрелостями и пеленочным дерматитом?
            </h2>
            <p>При этих состояниях первоочередная задача - облегчить дискомфорт ребенка, а также снизить риск развития инфекции. Для этого нужно выбрать оптимальное по действию средство, которое поможет подсушить поврежденный участок, убрать признаки воспаления и уменьшить страдания малыша от боли.</p>
            <p>
                Если это опрелости, то нужно особенно тщательно следить за режимом смены подгузников. Влажные салфетки следует оставить на случай прогулки или похода к гостям. Кожу на попке и в паху помойте под проточной водой, хорошо промокните, а затем нанесите подсушивающий крем. Цинка оксид, входящий в состав Судокрема, смягчает, обеззараживает и подсушивает участки с <a href="#link-4"><span class="under-title">проявлениями воспаления</span><sup>4</sup></a>. Врачи рекомендуют крем с содержанием окиси цинка для лечения опрелостей, пеленочного дерматита даже <a href="#link-2"><span class="under-title">новорожденным</span><sup>2</sup></a>. Судокрем является хорошим выбором также потому, что легко наносится и «не перегружает» тонкую кожу ребенка, это позволяет наносить крем тонким слоем <a href="#link-4"><span class="under-title">до 6 раз в сутки</span><sup>4</sup></a>. Другие компоненты в составе крема тоже выполняют важные функции: бензиловый спирт обеззараживает кожу от бактерий и грибков; бензилбензоат уменьшает <a href="#link-4"><span class="under-title">покраснение и боль</span><sup>4</sup></a>; вспомогательные вещества: воск и ланолин формируют водоотталкивающую основу, что помогает создать защитный слой от контакта с раздражителями. Кроме того, крем не «стягивает» кожу, имеет ненавязчивый запах. Использование Судокрема способствует заживлению пораженных участков и восстановлению кожи. Вследствие облегчения симптомов пеленочного дерматита и опрелостей улучшается общее состояние ребенка, его самочувствие и <a href="#link-3"><span class="under-title">настроение</span><sup>3</sup></a>. Кроме паховой области, Судокрем можно наносить малышу за ушки, на подколенные ямки и на шейные складки, если там возникает покраснение, связанное с показаниями. Нельзя наносить Судокрем на слизистую и <a href="#link-4"><span class="under-title">открытые раны</span><sup>4</sup></a>.
