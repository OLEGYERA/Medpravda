            <p>При уходе за новорожденным ребенком многие мамы сталкиваются с опрелостями на попке и в паху.</p>

            <h2>Почему у детей часто возникают проблемы с кожей?</h2>
            <p>
                Кожа малыша отличается от кожи взрослого человека. Она <a href="#link-1"><span class="under-title">тоньше</span><sup>1</sup></a>, содержит больше воды и меньше липидов, поэтому более чувствительна и нежна.
            </p>
            <h2>К чему это ведет?</h2>
            <p>Защитная функция кожи несовершенна, легко возникают повреждения кожных покровов, возможны инфекционные поражения кожи.</p>
            <p>В некоторых местах на коже малыша защитный слой весьма тонок, например, в области паха. Это влияет на способность кожи поддерживать слабокислый рН, который защищает кожу от инфекции.</p>

            <h2>Когда могут возникать покраснение кожи малыша?</h2>
            <div class="select">Например, в случае опрелостей и пеленочного дерматита</div>
            <p>
                Опрелости могут возникать при нарушении режима смены подгузников, если менять их реже, чем рекомендовано производителями подгузников. Внутри подгузника температура и влажность выше, что может привести к воспалению: кожа краснеет и становится горячей на ощупь. Опрелости создают дискомфорт малышу, он может плохо спать, есть и часто плакать. Необходимо принять меры, поскольку в месте воспаления кожа будет легко повреждаться инфекционными агентами и бактериями, грибками. Также пеленочный дерматит может возникнуть в ответ на постоянное раздражение: возможно, ребенку не подходит стиральный порошок или мыло, которым стирают ее одежду. Симптомы схожи с теми, что возникают при опрелостях: покраснение, беспокойство малыша, но область покраснения выглядит влажной. В случае присоединения грибковой или бактериальной инфекции воспаление быстро распространяется на бедра и живот. В таких случаях следует обязательно обратиться к врачу.
            </p>
