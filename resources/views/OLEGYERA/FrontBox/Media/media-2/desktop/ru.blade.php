            <h2>Для кого подходит ирригатор?</h2>
            <p>Ирригатор — это тот прибор, который необходим каждому человеку в целях профилактики, даже если сейчас у него нет никаких проблем с ротовой полостью и зубами. Кроме этого, стоматологи настоятельно рекомендуют его использование в определенных ситуациях:</p>
            <ol>
                <li>
                    <span class="num">1</span> Слабая эмаль зубов. Истонченная эмаль реагирует на кислое и сладкое, горячее и холодное и другие раздражители, на ней часто появляются эрозии из-за плохой чистки зубов. Ирригатор помогает комфортным способом справиться с проблемой.
                </li>
                <li>
                    <span class="num">2</span> Кровоточивость десен. Необходима усиленная гигиена и уход антисептиками, кроме того, струя массирует десна, ее можно регулировать.
                    Ношение брекет-систем. Очень важно соблюдение гигиены, существует специальная насадка для этих целей.

                </li>
                <li>
                    <span class="num">3</span> Наличие мостов. Это конструкция между двумя зубами. Там, где зубов больше нет, десна уходят вниз и скапливаются остатки еды, для их удаления есть специальные насадки.
                </li>
                <li>
                    <span class="num">4</span> Установка коронок и виниров. Десна вокруг зубов с коронками опускаются со временем, оголяется шейка зуба, промывание тонкой струей позволяет вычистить загрязнения в тех участках.
                </li>
                <li>
                    <span class="num">5</span> Наличие имплантов. Как и любая инородная конструкция во рту, имплант требует чистки. Ирригатор значительно облегчает процесс вживления импланта, так как помогает предотвратить возникновение воспаления.
                </li>
                <li>
                    <span class="num">6</span> Запах изо рта. Очень часто это случается именно по вине плохой гигиены ротовой полости, ирригатор проникнет в труднодоступные места, а насадка для языка справится с его очисткой.
                </li>
                <li>
                    <span class="num">7</span> Постоянное курение. Зубы страдают от этой вредной привычки, на них образуется налет, поэтому зубам курильщиков необходим особый уход.
                </li>
                <li>
                    <span class="num">8</span> Сахарный диабет в анамнезе. При диабете часто кровоточат десна, поэтому без ирригатора не обойтись.
                </li>
                <li>
                    <span class="num">9</span> Ношение брекет-систем. Очень важно соблюдение гигиены, существует специальная насадка для этих целей.
                </li>
            </ol>
